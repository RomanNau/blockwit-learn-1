package com.blockwit.learn1.romannau;

public class Helper {

    public void printArray(int[] mass) {
        for (int i = 0; i < mass.length; i++) {
            System.out.print(mass[i] + ",");
        }
    }

    public void printInList(int[] num) {
        for (int i = 0; i < num.length; i++) {
            System.out.print(num[i] + ", ");
        }
    }
}
